"""
Analytics API
=============
POST /analytics/event       — record a single user interaction event
POST /analytics/events/bulk — record multiple events in one request (batch)
GET  /analytics/stats       — admin: aggregated funnel & product stats
GET  /analytics/client/{client_id} — admin: full journey for one client
"""
