log = structlog.get_logger(__name__)
router = APIRouter()


# ═══════════════════════════════════════════════════════════════════════════
# Pydantic schemas
# ═══════════════════════════════════════════════════════════════════════════

class EventIn(BaseModel):
    """Payload the frontend sends for a single event."""

    event_type: str = Field(
        ...,
        pattern="^(product_view|cart_add|cart_remove|platform_redirect|search|checkout_start)$",
    )
    # Browser-persistent identity (generated once, stored in localStorage)
    client_id: str = Field(..., min_length=8, max_length=64)
    session_id: Optional[str] = Field(None, max_length=128)

    # What the user interacted with
    product_id: Optional[uuid.UUID] = None
    platform_id: Optional[uuid.UUID] = None

    # Event-specific payload
    price_shown: Optional[float] = None
    cart_item_count: Optional[int] = None
    search_query: Optional[str] = Field(None, max_length=500)
    referrer_page: Optional[str] = Field(None, max_length=100)
    redirect_url: Optional[str] = Field(None, max_length=2000)


class BulkEventsIn(BaseModel):
    events: List[EventIn] = Field(..., max_length=50)


class EventOut(BaseModel):
    id: uuid.UUID
    event_type: str
    client_id: str
    created_at: datetime

    model_config = {"from_attributes": True}


# ═══════════════════════════════════════════════════════════════════════════
# Helpers
# ═══════════════════════════════════════════════════════════════════════════

def _hash_ip(ip: str) -> str:
    """One-way hash of IP for privacy-safe storage."""
    return hashlib.sha256(ip.encode()).hexdigest()[:32]


def _client_ip(request: Request) -> str:
    forwarded = request.headers.get("X-Forwarded-For")
    if forwarded:
        return forwarded.split(",")[0].strip()
    return request.client.host if request.client else "unknown"


async def _persist_event(
    db: AsyncSession,
    payload: EventIn,
    user: Optional[User],
    request: Request,
) -> UserEvent:
    ip = _client_ip(request)
    event = UserEvent(
        event_type=payload.event_type,
        user_id=user.id if user else None,
        client_id=payload.client_id,
        session_id=payload.session_id,
        product_id=payload.product_id,
        platform_id=payload.platform_id,
        price_shown=payload.price_shown,
        cart_item_count=payload.cart_item_count,
        search_query=payload.search_query,
        referrer_page=payload.referrer_page,
        redirect_url=payload.redirect_url,
        ip_hash=_hash_ip(ip),
    )
    db.add(event)
    await db.commit()
    await db.refresh(event)
    return event


